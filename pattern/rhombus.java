import java.util.*;

public class rhombus {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        for(int  i=1;i<=5;i++){
            for(int j=5;j>i;j--){
                System.out.print(" ");
            }
        for(int k=1;k<=5;k++){
            System.out.print("*");
        }
        System.out.println();
        }
    }
}
